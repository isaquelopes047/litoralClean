let containerClick = document.querySelector('.cardInfos')
containerClick.addEventListener('click', () => {
    if (containerClick.style.height === '280px') {
        containerClick.style.height = '100px'
        containerClick.style.transition = 'height 0.3s ease-in-out';
    } else {
        containerClick.style.height = '280px'
        containerClick.style.transition = 'height 0.3s ease-in-out';
    }
})

